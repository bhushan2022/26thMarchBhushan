package Loops;

public class ForLoopStudy1 {

	public static void main(String[] args) 
	{
		// i want to print 6's table in reverse order
		for(int a=60;a>=0;a=a-6)
		{
		System.out.println(a);	
		}
		// i want to print bhushan jadhav in 3 times
		for(int a=1;a<=3;a++) 
		{
		System.out.println("bhushan Jadhav");
		}
		
		// I want to 27's table in incremntal order
		for(int a=27;a<=270;a=a+27)	
		{
			System.out.println(a);
		}
		//i want to 29's table in decremental order
		for(int r=290;r>=29;r=r-29)
		{
			System.out.println(r);
		}
		// i want to print Rudra satre in 100 times
		for(int a=1;a<=10;a++)
		{
			System.out.println("Rudra satre");
		}
		

	}

}
