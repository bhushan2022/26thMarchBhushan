package Loops;

public class WhileLoopStudy {

	public static void main(String[] args) {
		// I want to print 10's table incremental order
		int i=10;
		while(i<=100) //
		{
			System.out.println(i);
			i=i+10; //20.....90,100,110
			}
		System.out.println("=============");

		// I want to print 10's table Decremental order
		int j=100;
		while(j>=10)
		{
			System.out.println(j);
			j=j-10;
		}
		System.out.println("================");
	}

}
