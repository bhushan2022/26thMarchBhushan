package casting;

public class ExplicitCasting {

	public static void main(String[] args) 
	{
// converting higher datatype info into lower datatype info is called as Explicit casting
// Explicit casting is called as narrowing casting
// where memory size goes on decreasing
// in Explicit casting data loss takes place
		
		double p=99.9;
		System.out.println(p);
		
		int q=(int) p;
		System.out.println(q);
		
		ExplicitCasting e=new ExplicitCasting();
		// data type - object create= object class(memory allocate)

	}

}
