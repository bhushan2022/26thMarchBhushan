package casting;

public class ImplicitCasting {

	public static void main(String[] args)
	{
// converting lower datatype info into higher datatype info is called implicit casting
// Implicit casting is also called widening casting
// where memory size goes on increasing there is no data loss
		
		int a=10;
		System.out.println(a);
		double b=a;
		System.out.println(b);
	}

}
